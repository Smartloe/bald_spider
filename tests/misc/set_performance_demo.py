from timeit import timeit  # 导入时间测量模块

s = set()  # 创建一个空集合


def demo():
    """
    演示函数：执行集合的添加和删除操作

    功能：
    1. 向集合s中添加元素1
    2. 从集合s中删除元素1
    """
    s.add(1)  # 向集合添加元素1
    s.remove(1)  # 从集合删除元素1


def run():
    """
    运行性能测试函数

    使用timeit测量demo函数执行10^12次所需的时间
    注意：这个数字极大，实际运行可能需要非常长的时间
    """
    print(timeit(demo, number=10000000000))  # 执行1万亿次并打印耗时


# 程序入口点
if __name__ == "__main__":
    run()

